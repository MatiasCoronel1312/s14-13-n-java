CREATE TABLE destinations (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    image VARCHAR(600),
    city VARCHAR(600),
    title VARCHAR(600),
    subtitle VARCHAR(600),
    location VARCHAR(600),
    location_Title VARCHAR(600),
    high_Season VARCHAR(600),
    high_Season_Title VARCHAR(600),
    celebrations VARCHAR(600),
    celebrations_Title VARCHAR(600),
    phase VARCHAR(600)
);
